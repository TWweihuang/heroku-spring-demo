create sequence booking_information_sequence start 1;

CREATE TABLE BOOKING_INFORMATION
(
    BOOKING_ID     BIGINT GENERATED BY DEFAULT AS IDENTITY,
    BOOKING_NUMBER VARCHAR(255),
    USER_ID        BIGINT,
    PARKING_LOT_ID BIGINT,
    LOCATION_ID    BIGINT,
    SLOT_ID        BIGINT,
    RESERVED_DATE  VARCHAR(255),
    RESERVED_TIME  TIME,
    CREATION_DATE  TIMESTAMP,
    IS_PARKED      BOOLEAN,
    STATUS         VARCHAR(255),
    LEAVE_TIME     TIME
);
